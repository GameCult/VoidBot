function mapActivationProfile(
  profile: VoidSelfStateTypedProjection["selfProfile"]["activationProfile"],
): JsonValue {
  return {
    underlyingOrganization: mapActivationCategory(profile.underlyingOrganization),
    stableDispositions: mapActivationCategory(profile.stableDispositions),
    behavioralDimensions: mapActivationCategory(profile.behavioralDimensions),
    presentationStrategy: mapActivationCategory(profile.presentationStrategy),
    voiceStyle: mapActivationCategory(profile.voiceStyle),
    situationalState: mapActivationCategory(profile.situationalState),
  };
}

function mapActivationCategory(
  category: Record<string, { mean: number; plasticity: number; current_activation: number }>,
): JsonValue {
  return Object.fromEntries(
    Object.entries(category).map(([key, value]) => [
      key,
      scaleWord(value.mean),
    ]),
  );
}

