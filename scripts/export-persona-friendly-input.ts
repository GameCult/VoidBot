function inferVoiceSummary(description: string | undefined): string {
  if (!description) {
    return "";
  }
  const sentence = description
    .split(/(?<=[.!?])\s+/)
    .find((line) => /should|voice|speak|herald|pride|tone/i.test(line));
  return sentence?.trim() ?? "";
}

function inferImportance(tags: string[]): string {
  if (tags.some((tag) => /identity|value|doctrine/i.test(tag))) {
    return "high";
  }
  return "medium";
}

function sourceBlock(input: {
  id?: string;
  kind?: string;
  status?: string;
  target?: JsonValue;
  createdAt?: string;
  updatedAt?: string;
  resolvedAt?: string;
  retiredAt?: string;
  tags?: string[];
  anchorRefs?: JsonValue;
  evidenceRefs?: JsonValue;
  sourceMemoryIds?: string[];
}): JsonValue | undefined {
  if (!includeItemSource) {
    return undefined;
  }
  return stripUndefined({
    id: input.id,
    kind: input.kind,
    status: input.status,
    target: input.target,
    createdAt: input.createdAt,
    updatedAt: input.updatedAt,
    resolvedAt: input.resolvedAt,
    retiredAt: input.retiredAt,
    tags: nonEmptyArray(input.tags),
    anchors: mapRefs(input.anchorRefs),
    evidence: mapRefs(input.evidenceRefs),
    sourceMemoryIds: nonEmptyArray(input.sourceMemoryIds),
  });
}

function nonEmptyArray<T>(value: T[] | undefined): T[] | undefined {
  return value && value.length > 0 ? value : undefined;
}

function targetLabel(target: { label?: string; id: string }): string {
  return target.label ?? target.id;
}

function normalizeOptionalQuestion(value: string | undefined): string | undefined {
  if (!value || value.trim().toLowerCase() === "none.") {
    return undefined;
  }
  return value;
}

function mapRefs(refs: JsonValue | undefined): JsonValue | undefined {
  if (!Array.isArray(refs) || refs.length === 0) {
    return undefined;
  }
  return refs.map((entry) => {
    if (!entry || typeof entry !== "object" || Array.isArray(entry)) {
      return entry;
    }
    const ref = entry.ref;
    const kind = entry.kind;
    const summary = entry.summary;
    return typeof kind === "string" || typeof summary === "string"
      ? stripUndefined({ ref, kind, summary })
      : ref;
  });
}

function maybeScaleWord(value: number | undefined): string | undefined {
  return value === undefined ? undefined : scaleWord(value);
}

function scaleWord(value: number): "very_low" | "low" | "medium" | "high" | "very_high" {
  if (value < 0.2) {
    return "very_low";
  }
  if (value < 0.4) {
    return "low";
  }
  if (value < 0.65) {
    return "medium";
  }
  if (value < 0.85) {
    return "high";
  }
  return "very_high";
}

function valenceWord(value: number): "negative" | "mixed_negative" | "neutral" | "mixed_positive" | "positive" {
  if (value <= -0.6) {
    return "negative";
  }
  if (value < -0.15) {
    return "mixed_negative";
  }
  if (value <= 0.15) {
    return "neutral";
  }
  if (value < 0.6) {
    return "mixed_positive";
  }
  return "positive";
}

function latestTimestamp(values: Array<string | undefined>): string {
  return values
    .filter((value): value is string => Boolean(value))
    .sort((left, right) => left.localeCompare(right))
    .at(-1) ?? new Date(0).toISOString();
}

function stripUndefined<T extends JsonValue | undefined>(value: T): T {
  if (Array.isArray(value)) {
    return value.map((entry) => stripUndefined(entry)) as T;
  }
  if (!value || typeof value !== "object") {
    return value;
  }
  const output: Record<string, JsonValue> = {};
  for (const [key, child] of Object.entries(value)) {
    if (child !== undefined) {
      output[key] = stripUndefined(child as JsonValue);
    }
  }
  return output as T;
}

function toYaml(value: JsonValue, indent = 0): string {
  const pad = " ".repeat(indent);
  if (Array.isArray(value)) {
    if (value.length === 0) {
      return "[]";
    }
    return value
      .map((entry) => {
        if (isScalar(entry)) {
          return `${pad}- ${formatScalar(entry)}`;
        }
        const rendered = toYaml(entry, indent + 2);
        return `${pad}- ${rendered.trimStart()}`;
      })
      .join("\n");
  }
  if (value && typeof value === "object") {
    const entries = Object.entries(value).filter(([, child]) => child !== undefined);
    if (entries.length === 0) {
      return "{}";
    }
    return entries
      .map(([key, child]) => {
        if (isScalar(child)) {
          return `${pad}${key}: ${formatScalar(child)}`;
        }
        const rendered = toYaml(child as JsonValue, indent + 2);
        if (rendered === "[]" || rendered === "{}") {
          return `${pad}${key}: ${rendered}`;
        }
        return `${pad}${key}:\n${rendered}`;
      })
      .join("\n");
  }
  return formatScalar(value);
}

function isScalar(value: unknown): value is null | boolean | number | string {
  return value === null || ["boolean", "number", "string"].includes(typeof value);
}

function formatScalar(value: null | boolean | number | string): string {
  if (value === null) {
    return "null";
  }
  if (typeof value === "boolean" || typeof value === "number") {
    return String(value);
  }
  return JSON.stringify(value);
}

async function readIdentity(path: string): Promise<RepoFaceIdentity> {
  const raw = await readFile(path, "utf8");
  return JSON.parse(raw) as RepoFaceIdentity;
}

async function resolveIdentity(options: CliOptions): Promise<RepoFaceIdentity> {
  const fileIdentity = options.identityPath
    ? await readIdentity(options.identityPath)
    : {};

  return {
    ...fileIdentity,
    identityId: options.identityId ?? fileIdentity.identityId,
    displayName: options.displayName ?? fileIdentity.displayName,
    repoName: options.repoName ?? fileIdentity.repoName,
    avatarUrl: options.avatarUri ?? fileIdentity.avatarUrl,
    repoPath: options.repoPath ?? fileIdentity.repoPath,
  };
}

function parseArgs(args: string[]): CliOptions {
  const options: CliOptions = {
    includeSource: false,
    identityPath: DEFAULT_IDENTITY_PATH,
    statePath: DEFAULT_STATE_PATH,
    outPath: DEFAULT_OUT_PATH,
  };

  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index];
    const value = args[index + 1];
    if (arg === "--identity" && value) {
      options.identityPath = value;
      index += 1;
    } else if (arg === "--no-identity-file") {
      options.identityPath = undefined;
    } else if (arg === "--id" && value) {
      options.identityId = value;
      index += 1;
    } else if (arg === "--name" && value) {
      options.displayName = value;
      index += 1;
    } else if (arg === "--repo-name" && value) {
      options.repoName = value;
      index += 1;
    } else if (arg === "--avatar-uri" && value) {
      options.avatarUri = value;
      index += 1;
    } else if (arg === "--repo-path" && value) {
      options.repoPath = value;
      index += 1;
    } else if (arg === "--include-source") {
      options.includeSource = true;
    } else if (arg === "--state" && value) {
      options.statePath = value;
      index += 1;
    } else if (arg === "--out" && value) {
      options.outPath = value;
      index += 1;
    } else if (arg === "--help" || arg === "-h") {
      printUsage();
      process.exit(0);
    } else {
      throw new Error(`Unknown or incomplete argument: ${arg}`);
    }
  }

  return options;
}

function printUsage(): void {
  console.log([
    "Usage: npx tsx scripts/export-persona-friendly-input.ts [--identity path | --no-identity-file] [--state path] [--out path]",
    "",
    `Defaults:`,
    `  --identity ${DEFAULT_IDENTITY_PATH}`,
    `  --state    ${DEFAULT_STATE_PATH}`,
    `  --out      ${DEFAULT_OUT_PATH}`,
    "",
    "Native Persona options:",
    "  --no-identity-file --state .voidbot/private/personas/metame/metame.cc --out docs/persona-intake/metame.persona-intake.yaml",
    "",
    "Provenance:",
    "  --include-source   Include per-item parse-back IDs, timestamps, tags, and anchors.",
  ].join("\n"));
}

main(process.argv.slice(2)).catch((error) => {
  console.error(error instanceof Error ? error.stack ?? error.message : String(error));
  process.exitCode = 1;
});
